u16 Timer_Get_Count(u8 id)
{
  if (id == COUNT_BEAT_ID)
    return Count_Beat;
  return 65535;
}

void Timer_Set_Count(u8 id, u16 value)
{
  if (id == COUNT_BEAT_ID)
    Count_Beat = value;
}

void Timer_Count_Auto_Reduce(u8 id)
{
  if (id == COUNT_BEAT_ID)
    Count_Beat--;
}


void BUZZER_ON(void)
{
	__HAL_TIM_SET_COMPARE(&htim4,TIM_CHANNEL_3,30);
}

void BUZZER_OFF(void)
{
	__HAL_TIM_SET_COMPARE(&htim4,TIM_CHANNEL_3,0);
}


void Buzzer_Reset(void)
{
	BUZZER_OFF();
	for(int i = 0;i<3;i++)
	{
		pBuzzer[i]->Buzzer_Flag = 0;
	}
	Buzzer_cnt = 0;
	Buzzer_time = 0;
}


